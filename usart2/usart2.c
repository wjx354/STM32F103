int32_t distance_left;
int32_t degree_left;

int32_t distance_right;
int32_t degree_right;

uint8_t Uart2_head1;
uint8_t Uart2_head2;

uint8_t sta1;
uint8_t sta2;

uint8_t left_or_right;
 
uint8_t Uart2_Buffer[128];        //接收缓冲区 

uint8_t Uart2_Rx; 



void uart2_init(u32 bound){
  //GPIO端口设置
  GPIO_InitTypeDef GPIO_InitStructure;
	USART_InitTypeDef USART_InitStructure;
	NVIC_InitTypeDef NVIC_InitStructure;
	 
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOA, ENABLE);
  RCC_APB1PeriphClockCmd(RCC_APB1Periph_USART2, ENABLE);    
	
	//USART2_TX   GPIOA.2
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_2; //PA.2
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;	//复用推挽输出
  GPIO_Init(GPIOA, &GPIO_InitStructure);//初始化GPIOA.2
   
  //USART2_RX	  GPIOA.3初始化
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_3;//PA3
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;//浮空输入
  GPIO_Init(GPIOA, &GPIO_InitStructure);//初始化GPIOA.3 

  //Usart1 NVIC 配置
  NVIC_InitStructure.NVIC_IRQChannel = USART2_IRQn;
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority=3 ;//抢占优先级3
	NVIC_InitStructure.NVIC_IRQChannelSubPriority = 2;		//子优先级3
	NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;			//IRQ通道使能
	NVIC_Init(&NVIC_InitStructure);	//根据指定的参数初始化VIC寄存器
  
   //USART 初始化设置

	USART_InitStructure.USART_BaudRate = bound;//串口波特率
	USART_InitStructure.USART_WordLength = USART_WordLength_8b;//字长为8位数据格式
	USART_InitStructure.USART_StopBits = USART_StopBits_1;//一个停止位
	USART_InitStructure.USART_Parity = USART_Parity_No;//无奇偶校验位
	USART_InitStructure.USART_HardwareFlowControl = USART_HardwareFlowControl_None;//无硬件数据流控制
	USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;	//收发模式

  USART_Init(USART2, &USART_InitStructure); //初始化串口2
  USART_ITConfig(USART2, USART_IT_RXNE, ENABLE);//开启串口接受中断
  USART_Cmd(USART2, ENABLE);                    //使能串口2

}

void USART2_IRQHandler(void)  
{  
  if(USART_GetFlagStatus(USART2, USART_FLAG_RXNE) == SET)  
  {   
		
		Uart2_Buffer[Uart2_Rx] = USART_ReceiveData(USART2); 
		Uart2_Rx++;		
		//USART_SendData(USART1,USART_ReceiveData(USART2));
  }
	
	if(Uart2_Buffer[Uart2_Rx-1] == 0x66)   //判断帧头1
		Uart2_head1 = Uart2_Rx-1;
	
	else if(Uart2_Rx-1 == Uart2_head1+1) //判断帧头1数据后是否为帧头2
	{
		if(Uart2_Buffer[Uart2_Rx-1] == 0x00)
		{sta1 = 1;left_or_right=1;}
		else if(Uart2_Buffer[Uart2_Rx-1] == 0x01)
		{sta1 = 1;left_or_right=2;}
		else{
			Uart2_Rx=0;
			sta1=0;
			sta2=0;
			Uart2_head1=0;
			Uart2_head2=0;
			left_or_right=0;}
	}
	
	else if(Uart2_Rx-1 == Uart2_head1+5)
	{
		if(Uart2_Buffer[Uart2_Rx-1] == 0x70)
		{Uart2_head2=Uart2_Rx-1;}
		else {		
			Uart2_Rx=0;
			sta1=0;
			sta2=0;
			Uart2_head1=0;
			Uart2_head2=0;
			left_or_right=0;}
	}
	
	else if(Uart2_Rx-1 == Uart2_head2+1) //判断帧头1数据后是否为帧头2
	{
		if(Uart2_Buffer[Uart2_Rx-1] == 0x76)
		{sta2 = 1;}
		else{
			Uart2_Rx=0;
			sta1=0;
			sta2=0;
			Uart2_head1=0;
			Uart2_head2=0;
			left_or_right=0;}
	}
	
	
	
	if(sta1&&sta2)
	{
		if(left_or_right==1)
		{
		distance_left=Uart2_Buffer[Uart2_head1+2];
		degree_left=(Uart2_Buffer[Uart2_head1+3]<<8)|Uart2_Buffer[Uart2_head1+4];
		
		Uart2_Rx=0;
		sta1=0;
		sta2=0;
		Uart2_head1=0;
		Uart2_head2=0;
		left_or_right=0;
		}
		else if(left_or_right==2)
		{
		distance_right=Uart2_Buffer[Uart2_head1+2];
		degree_right=(Uart2_Buffer[Uart2_head1+3]<<8)|Uart2_Buffer[Uart2_head1+4];
		
		Uart2_Rx=0;
		sta1=0;
		sta2=0;
		Uart2_head1=0;
		Uart2_head2=0;
		left_or_right=0;
		}
	}
}
